<?php
/**
 * Useful numeric functions.
 */
var_dump(abs(-4.2)); // Output: 4.2
var_dump(abs("5")); // Output: 5
var_dump(base_convert(127, 10, 2)); // Output: 1111111
var_dump(base_convert("ff7a", 16, 10)); // Output: 65402
var_dump(ceil(4.2)); // Output: 5
var_dump(floor(4.2)); // Output: 4
var_dump(round(pi(), 4)); // Output: 4

